/// Spacing tokens for the application shell.
abstract final class AppSpacing {
  const AppSpacing._();

  /// Extra small spacing (4.0).
  static const double xs = 4;

  /// Small spacing (8.0).
  static const double sm = 8;

  /// Medium spacing (12.0).
  static const double md = 12;

  /// Large spacing (16.0).
  static const double lg = 16;

  /// Extra large spacing (20.0).
  static const double xl = 20;

  /// Double extra large spacing (24.0).
  static const double xxl = 24;

  /// 3x large spacing (32.0).
  static const double x3l = 32;

  /// 4x large spacing (40.0).
  static const double x4l = 40;

  /// 5x large spacing (48.0).
  static const double x5l = 48;

  /// 6x large spacing (64.0).
  static const double x6l = 64;

  /// 7x large spacing (80.0).
  static const double x7l = 80;
}
